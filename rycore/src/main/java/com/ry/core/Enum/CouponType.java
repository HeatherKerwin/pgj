package com.ry.core.Enum;

/**
 * 红包类型（GENERAL通用红包、DISC贴现红包、DIST接单红包、INQUIRYREPLY查询查复红包）
 * @author MH
 */
public enum CouponType{
	
	GENERAL(0,"通用红包"),DISC(1,"贴现红包"),DIST(2,"接单红包"),INQUIRYREPLY(3,"查询查复红包");
	
	private Integer key;
	private String value;
	
	CouponType(Integer key,String value){
		this.key = key;
		this.value = value;
	}
	
	public Integer getKey() {
		return key;
	}
	
	public void setKey(Integer key) {
		this.key = key;
	}
	
	public String getValue() {
		return value;
	}
	
	public void setValue(String value) {
		this.value = value;
	}
}