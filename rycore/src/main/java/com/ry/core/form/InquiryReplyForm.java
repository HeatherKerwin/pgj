package com.ry.core.form;

import java.math.BigDecimal;

import com.ry.core.entity.InquiryReply;

/**
 * 动态查询查复
 * @author KHC
 */
public class InquiryReplyForm extends InquiryReply{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String start;//起始时间
	private String end;//介绍时间
	private BigDecimal minMoney;//最小金额
	private BigDecimal maxMoney;//最大金额
	private Integer num;//
	
	public String getStart() {
		return start;
	}
	
	public void setStart(String start) {
		this.start = start;
	}
	
	public String getEnd() {
		return end;
	}
	
	public void setEnd(String end) {
		this.end = end;
	}
	
	public BigDecimal getMinMoney() {
		return minMoney;
	}
	
	public void setMinMoney(BigDecimal minMoney) {
		this.minMoney = minMoney;
	}
	
	public BigDecimal getMaxMoney() {
		return maxMoney;
	}
	
	public void setMaxMoney(BigDecimal maxMoney) {
		this.maxMoney = maxMoney;
	}

	public Integer getNum() {
		return num;
	}

	public void setNum(Integer num) {
		this.num = num;
	}
}
