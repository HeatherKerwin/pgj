package com.ry.core.form;

import com.ry.util.datatable.BasePageRequestData;

/**
 * @author MH
 *客服报表的form类
 */
public class CustomReportForm extends BasePageRequestData{

	private String orgcompany;//机构名称
	private String bnscompany;//企业名称
	private String startDate;//登陆的开始时间
	private String endDate;//登陆的截至时间
	private Integer price;//是否报价（1：未报价，2已报价）
	private Integer discount;//是否贴现（1：未贴现，2已贴现）
	
	public String getOrgcompany() {
		return orgcompany;
	}
	public void setOrgcompany(String orgcompany) {
		this.orgcompany = orgcompany;
	}
	public String getBnscompany() {
		return bnscompany;
	}
	public void setBnscompany(String bnscompany) {
		this.bnscompany = bnscompany;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	public Integer getDiscount() {
		return discount;
	}
	public void setDiscount(Integer discount) {
		this.discount = discount;
	}
	
}
