package com.utiexian.utils.enums;

/**
 * 票据类型（YZ银纸、YD银电、SZ商纸、SD商电）
 * @author WKX
 */
public enum DraftType{
	
	YZ(0,"银纸"),YD(1,"银电"),SZ(2,"商纸"),SD(2,"商电");
	
	private Integer key;
	private String value;
	
	DraftType(Integer key,String value){
		this.key = key;
		this.value = value;
	}
	
	public Integer getKey() {
		return key;
	}
	
	public void setKey(Integer key) {
		this.key = key;
	}
	
	public String getValue() {
		return value;
	}
	
	public void setValue(String value) {
		this.value = value;
	}
}