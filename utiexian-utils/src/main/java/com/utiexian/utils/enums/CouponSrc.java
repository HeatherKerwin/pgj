package com.utiexian.utils.enums;

/**
 * 红包来源（LUCK手气红包、GIVE赠送、ACTIVITY活动、REGISTER新用户注册）
 * @author WKX
 */
public enum CouponSrc{
	
	LUCK(0,"手气红包"),GIVE(1,"赠送"),ACTIVITY(2,"活动"),REGISTER(3,"新用户注册");
	
	private Integer key;
	private String value;
	
	CouponSrc(Integer key,String value){
		this.key = key;
		this.value = value;
	}
	
	public Integer getKey() {
		return key;
	}
	
	public void setKey(Integer key) {
		this.key = key;
	}
	
	public String getValue() {
		return value;
	}
	
	public void setValue(String value) {
		this.value = value;
	}
}